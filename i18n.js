function normalizeGeekezLang(input) {
    const text = String(input || '').trim().toLowerCase();
    if (!text) return 'cn';
    if (text === 'en' || text === 'english' || text.startsWith('en-')) return 'en';
    if (
        text === 'cn' ||
        text === 'zh' ||
        text === 'zh-cn' ||
        text === 'zh_cn' ||
        text === 'chinese' ||
        text === '中文' ||
        text.startsWith('zh-')
    ) return 'cn';
    return 'cn';
}

// Global i18n Manager
window.normalizeGeekezLang = normalizeGeekezLang;
window.curLang = normalizeGeekezLang(localStorage.getItem('geekez_lang'));
localStorage.setItem('geekez_lang', window.curLang);

window.t = function (key) {
    if (window.curLang === 'cn' && window.zhCN && window.zhCN[key]) {
        return window.zhCN[key];
    }
    return enTranslations[key] || key;
};

window.toggleLanguage = function () {
    window.curLang = window.curLang === 'cn' ? 'en' : 'cn';
    localStorage.setItem('geekez_lang', window.curLang);
    location.reload();
};
